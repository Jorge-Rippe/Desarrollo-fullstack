// operadores aritméticos
console.log(5 + 5); // suma
console.log(5 - 5); // resta
console.log(5 * 5); // multiplicación
console.log(5 / 5); // división
console.log(5 ** 5); // potencia
//operadores boleanos
console.log(5 > 5); // mayor que
console.log(5 < 5); // menor que
console.log(5 == 5); // igual a
console.log(true); // verdadero
console.log(false); // falso
console.log(!false); // negación
console.log(!true); // negación
// operadores de relación
console.log(5 >= 5); // mayor o igual que
console.log(5 <= 5); // menor o igual que
console.log(50 != 5); // diferente de
